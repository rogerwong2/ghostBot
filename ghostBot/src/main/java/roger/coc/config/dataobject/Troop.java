package roger.coc.config.dataobject;

public class Troop {
	private String troop;
	private int num;
	/**
	 * @return the troop
	 */
	public String getTroop() {
		return troop;
	}
	/**
	 * @param troop the troop to set
	 */
	public void setTroop(String troop) {
		this.troop = troop;
	}
	/**
	 * @return the num
	 */
	public int getNum() {
		return num;
	}
	/**
	 * @param num the num to set
	 */
	public void setNum(int num) {
		this.num = num;
	}
}
